package javaprograms;

public class Swapnumbers {
    public static void main(String[] args) {
        int a =10;
        int b= 20;
        System.out.println("before swap.... " +a +" " + b);

        int t=a;
        a=b;
        b=t;
        System.out.println("After swap" + a + " " + b);
    }


}
